package designPatternsAndPrinciples.classes;

/**
 * La classe {@code Primate} rappresenta un primate e fornisce un metodo
 * {@code haIPeli()} che restituisce sempre {@code true}, indicando che i
 * primati hanno peli.
 */
public class Primate {
	/**
	 * Metodo che restituisce sempre {@code true}, indicando che i primati hanno
	 * peli.
	 * 
	 * @return {@code true}  (i primati hanno peli)
	 */
	public boolean haIPeli() {
		return true;
	}
}
